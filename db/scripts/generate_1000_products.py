def generate_products(count=1000):
    """Return exactly 1000 products from the realistic catalog."""
    products = []
    for i in range(min(count, len(REALISTIC_PRODUCTS))):
        name, category, price, weight, perishable = REALISTIC_PRODUCTS[i]
        product_id = f"product:prod{i+1:04d}"
        products.append({
            'id': product_id,
            'name': name,
            'category': category,
            'perishable': perishable,
            'price': price,
            'weight': weight
        })
    return products

